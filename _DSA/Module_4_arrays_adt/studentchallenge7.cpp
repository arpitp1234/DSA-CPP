//1.Find a pair of element with sum k in sorted array

#include<iostream>
using namespace std;

int main(){
    int A[10]={1,3,4,5,6,8,9,10,11,12};
    int k=10;
    int n=10;
    int i=0,j=n-1;
    while(i<j){
        if(A[i]+A[j]==k){
            cout<<A[i]<<" +"<<A[j]<<" = "<<k<<endl;
            i++;
            j--;
        }else if(A[i]+A[j]<k){
            i++;
        }else if(A[i]+A[j]>k){
            j--;
        }
    }
    return 0;
}









































































































































































































































